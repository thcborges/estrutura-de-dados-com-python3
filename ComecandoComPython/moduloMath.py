import math

print(math.ceil(3.14))  # maior inteiro maior ou igual a um número

print(math.fabs(-4))  # valor absoluto de um número

print(math.floor(3.14))  # maior inteiro menor ou igual a um número

print(math.exp(2))  # resultado da exponenciação de e elevado a 2

print(math.log(1024, 2))  # logaritmo de 1024 na base 2

print(math.pow(2, 10))  # 2 elevado a 10

print(math.sqrt(25))  # raiz quadrada de 25

print(math.cos(0))  # cosseno de 0

print(math.sin(30))  # seno de 30

print(math.pi)  # PI

print(math.e)  # número de Euler
